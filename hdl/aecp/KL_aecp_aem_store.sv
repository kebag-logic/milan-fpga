/*
------------------------------------------------------------------------------
  File        : KL_aecp_aem_store.sv
  Description : AEM descriptor store — byte-wide RAM holding the descriptor
                tree generated by avdecc/gen_aem_store.py (gen/aecp_aem_rom.svh).

                Read port: 1-cycle latency, byte granularity (the response
                builder streams descriptors one byte per cycle).
                Write port: byte granularity, used by the SET_* write-back
                (SET_NAME / SET_SAMPLING_RATE / SET_STREAM_FORMAT). Writes are
                a VOLATILE mirror tonight — the NV overlay / factory-reset
                flush of aem-and-aecp.md §NV is a follow-up; factory_reset_i
                is accepted but only reloads on FPGA reconfiguration.

                Live/runtime fields (entity_id, MAC, ...) are zero in the RAM
                and overlaid at read time by KL_aecp_aem_dyn_mux.

  Spec refs   : IEEE Std 1722.1-2021 §7.2; aem-and-aecp.md (4-level memory L3)
  Company     : Kebag Logic
  Project     : Milan ADP / AECP
------------------------------------------------------------------------------
*/
