`default_nettype none

import aecp_pkg::*;

module KL_aecp_aem_store (
  input  wire          clk_i,
  input  wire          rst_n,
  input  wire [15:0]   addr_i,
  input  wire          rd_i,
  output logic [7:0]   data_o,
  input  wire  [15:0]  wr_addr_i,
  input  wire          wr_i,
  input  wire  [7:0]   wr_data_i,
  input  wire          factory_reset_i,
  output logic         flush_in_progress_o
);

  `include "gen/aecp_aem_rom.svh"

  //! Descriptor image RAM (BRAM-inferred, initialised from the generator)
  logic [7:0] mem_r [0:AEM_ROM_BYTES_C-1];

  initial begin
    for (int k = 0; k < AEM_ROM_BYTES_C; k++) mem_r[k] = AEM_ROM_INIT_C[k];
  end

  always_ff @(posedge clk_i) begin
    if (rd_i)
      data_o <= mem_r[addr_i];
    if (wr_i)
      mem_r[wr_addr_i] <= wr_data_i;
  end

  //! Factory reset: volatile store — nothing to flush until the NV overlay
  //! lands; reconfiguration restores the generated image.
  assign flush_in_progress_o = 1'b0;

  // verilator lint_off UNUSED
  wire unused = &{1'b0, rst_n, factory_reset_i};
  // verilator lint_on  UNUSED

endmodule

`default_nettype wire
